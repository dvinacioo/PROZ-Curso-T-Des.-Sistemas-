# Tupla é uma estrutura de dados imutável, ou seja, após ser criada, seus elementos não podem ser alterados. 
# Elas podem conter diferentes tipos de dados e são definidas utilizando parênteses. 
# Tuplas são úteis quando você deseja garantir que os dados não sejam modificados acidentalmente.

# Exemplo de tupla:

estacoes = ('primavera', 'verão', 'outuno', 'inverno')
print(estacoes)
print(estacoes[3])