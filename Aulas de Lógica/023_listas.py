# Lista é uma estrutura de dados mutável, ou seja, seus elementos podem ser alterados após a criação.
# Listas são definidas usando colchetes e podem armazenar diferentes tipos de dados.
# São muito utilizadas em Python por sua flexibilidade e facilidade de uso.
# Exemplo de lista:

alunos = ['Davi', 'Pedro', 'Enrico', "Isabella"]

print("Terceiro aluno: ",alunos[2])
print("Quarto aluno: ",alunos[3])
print(" ")

for alunos in alunos:
    print(alunos)
